import numpy as np

A = np.array([10, 20, 30, 40])
B = np.array([1, 2, 3, 4])

print("Addition:", A + B)
print("Subtraction:", A - B)
print("Multiplication:", A * B)
print("Division:", A / B)
print("Power:", A ** 2)

# Optional NumPy functions
print("np.add:", np.add(A, B))
print("np.subtract:", np.subtract(A, B))
